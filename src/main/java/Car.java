public class Car {
     String carBrand;
     String carType;
     int carBuildYear;
     String carColor;
     float carEngineCapacity;

     public Car (String carBrand, String carType, int carBuildYear, String carColor, float carEngineCapacity) {
          this.carBrand = carBrand;
          this.carType = carType;
          this.carBuildYear = carBuildYear;
          this.carColor = carColor;
          this.carEngineCapacity = carEngineCapacity;
     }
}